/**
 * Verifica si una contraseña es común (vulnerable)
 * @param {string} password - Contraseña a verificar
 * @returns {boolean} - true si es una contraseña común
 */
export const isCommonPassword = (password) => {
  if (!password) return false;
  return COMMON_PASSWORDS.includes(password.toLowerCase());
};

/**
 * Valida la fortaleza de una contraseña según los requisitos del backend
 * Requisitos:
 * - Mínimo 8 caracteres
 * - Al menos una letra mayúscula
 * - Al menos una letra minúscula
 * - Al menos un número
 * - Al menos un carácter especial (!@#$%^&*)
 * - Máximo 128 caracteres
 * 
 * @param {string} password - Contraseña a validar
 * @returns {Object} - { isValid: boolean, errors: string[], strength: 'weak'|'medium'|'strong' }
 */
export const validatePasswordStrength = (password) => {
  const errors = [];
  
  if (!password) {
    errors.push('La contraseña es requerida');
    return {
      isValid: false,
      errors,
      strength: 'weak'
    };
  }

  // Validar longitud mínima
  if (password.length < 8) {
    errors.push('La contraseña debe tener al menos 8 caracteres');
  }

  // Validar longitud máxima
  if (password.length > 128) {
    errors.push('La contraseña no puede exceder 128 caracteres');
  }

  // Validar mayúsculas
  if (!/[A-Z]/.test(password)) {
    errors.push('La contraseña debe contener al menos una letra mayúscula');
  }

  // Validar minúsculas
  if (!/[a-z]/.test(password)) {
    errors.push('La contraseña debe contener al menos una letra minúscula');
  }

  // Validar números
  if (!/[0-9]/.test(password)) {
    errors.push('La contraseña debe contener al menos un número');
  }

  // Validar caracteres especiales
  if (!/[!@#$%^&*]/.test(password)) {
    errors.push('La contraseña debe contener al menos un carácter especial (!@#$%^&*)');
  }

  // Verificar si es una contraseña común
  if (isCommonPassword(password)) {
    errors.push('Esta contraseña es muy común y no es segura. Por favor, elige una contraseña más única');
  }

  // Calcular fortaleza
  let strength = 'weak';
  if (errors.length === 0) {
    if (password.length >= 12 && /[!@#$%^&*]/.test(password) && /[A-Z]/.test(password) && /[a-z]/.test(password) && /[0-9]/.test(password)) {
      strength = 'strong';
    } else if (password.length >= 8) {
      strength = 'medium';
    }
  }

  return {
    isValid: errors.length === 0,
    errors,
    strength
  };
};

